package com.aurosaswatraj.countmycrunch.CalorieCounter

class CalorieData(var Disptitle:String,var mfoodDisplay:String,var swipeNext:String) {

    fun getTitle():String{
        return Disptitle
    }


    fun getFoodDisplay():String{
        return mfoodDisplay
    }

    fun getswipeNext():String{
        return swipeNext
    }

    override fun toString(): String {
        return "CalorieData(${getTitle()},${getFoodDisplay()},${getswipeNext()})"
    }





}